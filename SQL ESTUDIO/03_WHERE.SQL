SELECT *
FROM users
WHERE age=15

SELECT user_id
from users
Where user_id=1

// El Where es una condicion (filtro) que se usa para solo traer de la base de datos
lo que se le pide, por ejemplo alguien que tenga edad 15, o que sea menor o mayor de 
'x' edad, o alguien donde la condicion sea que su user_id debe ser 1 //